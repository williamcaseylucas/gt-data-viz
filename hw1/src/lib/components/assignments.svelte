		<h2>Homework Assignments</h2>
		<p class="card">
			Details on HW assignments are on the Canvas site for this course. Due dates for each
			assignments can be found on Canvas and on the course Schedule. Grading distributions can be
			found on the course Syllabus. Submit all HW assignments on Canvas. Unless indicated by the HW
			instructions, all HWs are to be completed individually.

			<br /><br />
			These individual assignments will teach you the basic skills for developing web-based visualizations.
			You are expected to complete these assignments using d3.js.
			<br /><br />

			It is good practice to develop your assignments using some sort of version control. GaTech
			gives you access to GitHub, which is a good one to use if you havenâ€™t done so already.
			<br /><br />

			When grading, we will use Google Chrome in Incognito Mode to run your visualizations. Further,
			when a server is required, we will use a python server on localhost.
			<br /><br />
			When submitting on Canvas, make sure you submit a .zip containing all your files, and name it Lastname_Firstname.zip
			(e.g., Endert_Alex.zip), unless otherwise mentioned in the assignment.
			<br /><br />

			Warning: There are many existing examples and source code widely available online. While these
			are great resources for you to learn, note that copying these is considered a breach of the
			rules from the Office of Student Integrity, and will be handled accordingly. Be careful and
			thoughtful to use these resources responsibly.
		</p>
